.data
    prompt: .asciiz "Digite um numero inteiro positivo: "
    result: .asciiz "O fatorial eh: "

.text
    main:
        li $v0, 4            # 001100 00000 00100 0000000000000100
        la $a0, prompt       # 001111 00000 00100 0000000000000000
        syscall

        li $v0, 5            # 001100 00000 00101 0000000000000101
        syscall
        move $t0, $v0        # 000000 00101 00000 01000 00000 100001

        li $t1, 1            # 001000 00000 01001 0000000000000001

    loop:
        beq $t0, $zero, exit # 000100 01000 00000 0000000000001010
        mul $t1, $t1, $t0    # 000000 01000 01001 01000 00000 000010
        addi $t0, $t0, -1    # 001000 01000 01000 1111111111111111
        j loop               # 000010 00000000000001000000000000

    exit:
        li $v0, 4            # 001100 00000 00100 0000000000000100
        la $a0, result       # 001111 00000 00100 0000000000000011
        syscall

        li $v0, 1            # 001100 00000 00001 0000000000000001
        move $a0, $t1        # 000000 00001 01001 01000 00000 100001
        syscall

        li $v0, 10           # 001100 00000 01010 0000000000001010
        syscall              # 000000 00000000000000000000001100
